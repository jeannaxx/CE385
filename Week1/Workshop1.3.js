let Math = 85;
let Sci= 72;
let Eng  = 90;
let sum = Math+Sci+Eng  ; // sumคือการบวก จึงกำหนดตัวแปรตัวรวม เพาระว่ากันกรณีเปลี่ยนตัวเลขจะทำให้ค่าเปลี่ยนเเปลงตามไป

console.log("คะเเนนรวม:" ,sum); //เรียกsum มาจะทำกัยผลรวม 
console.log("คะเเนนเฉลี่ย:", sum /3 ); //หาร3เพาระมี3วิชา ได้ค่าเฉลี่ยออกมา
console.log("คะเเนนเฉลี่ย >= 80 ",sum >= 80 ); // เช็คว่า ค่าsumมากกว่าหรือเท่ากับ 80 ไหม ถ้าใช่จะtrue 